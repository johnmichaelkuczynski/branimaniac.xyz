export function getQuotesForThinker(thinkerName: string, figureId?: string): string[] {
  // Try figureId first
  if (figureId && THINKER_QUOTES[figureId]) {
    return THINKER_QUOTES[figureId];
  }
  // Try full name
  if (THINKER_QUOTES[thinkerName]) {
    return THINKER_QUOTES[thinkerName];
  }
  // Try lowercase match
  const lowerName = thinkerName.toLowerCase();
  for (const key of Object.keys(THINKER_QUOTES)) {
    if (key.toLowerCase() === lowerName || lowerName.includes(key.toLowerCase())) {
      return THINKER_QUOTES[key];
    }
  }
  return DEFAULT_QUOTES;
}
